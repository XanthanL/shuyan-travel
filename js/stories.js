// 全局辅助函数
window.getStory = (id) => storiesDatabase[id];
window.getAllStories = () => Object.values(storiesDatabase).sort((a, b) => {
    const dateA = new Date(a.date.replace(/\./g, '/'));
    const dateB = new Date(b.date.replace(/\./g, '/'));
    return dateB - dateA;
});
window.getAdjacentStories = (currentId) => {
    const all = window.getAllStories();
    const index = all.findIndex(s => s.id.toString() === currentId.toString());
    return {
        prev: index > 0 ? all[index - 1] : null,
        next: index < all.length - 1 ? all[index + 1] : null
    };
};
