// 获取故事数据
function getStory(storyId) {
    return storiesDatabase[storyId] || storiesDatabase["1"];
}

// 获取所有故事列表
function getAllStories() {
    return Object.values(storiesDatabase).sort((a, b) => b.id - a.id);
}

// 获取相邻故事
function getAdjacentStories(storyId) {
    const story = getStory(storyId);
    return {
        prev: story.prevId ? getStory(story.prevId) : null,
        current: story,
        next: story.nextId ? getStory(story.nextId) : null
    };
}

// 搜索故事
function searchStories(keyword) {
    keyword = keyword.toLowerCase();
    return Object.values(storiesDatabase).filter(story => {
        return story.title.toLowerCase().includes(keyword) ||
               story.subtitle.toLowerCase().includes(keyword) ||
               story.location.toLowerCase().includes(keyword) ||
               story.tags.some(tag => tag.toLowerCase().includes(keyword)) ||
               story.content.toLowerCase().includes(keyword);
    });
}

// 按标签筛选故事
function filterStoriesByTag(tag) {
    tag = tag.toLowerCase();
    return Object.values(storiesDatabase).filter(story => 
        story.tags.some(storyTag => storyTag.toLowerCase() === tag)
    );
}

// 按地点筛选故事
function filterStoriesByLocation(location) {
    location = location.toLowerCase();
    return Object.values(storiesDatabase).filter(story => 
        story.location.toLowerCase().includes(location)
    );
}

// 获取故事统计
function getStoryStats() {
    const stories = Object.values(storiesDatabase);
    return {
        total: stories.length,
        totalReadTime: stories.reduce((sum, story) => {
            const minutes = parseInt(story.readTime) || 0;
            return sum + minutes;
        }, 0),
        locations: [...new Set(stories.map(story => story.location.split('·')[0].trim()))],
        tags: [...new Set(stories.flatMap(story => story.tags))],
        years: [...new Set(stories.map(story => story.date.split('.')[0]))].sort((a, b) => b - a)
    };
}

